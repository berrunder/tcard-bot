package main

import "testing"

func TestExtractNumber(t *testing.T) {
	cases := []struct {
		in, want string
	}{
		{"Hello, world", ""},
		{"9643102303300612648", "9643102303300612648"},
		{"9643 10230 33006 12648", "9643102303300612648"},
		{" 9643   10230  33006    12648", "9643102303300612648"},
		{"Проверь 9643102303300612648", "9643102303300612648"},
		{"Проверь 9643 10230 33006 12648", "9643102303300612648"},
		{"9643102303300612648 мой номер", "9643102303300612648"},
		{"9643 10230 33006 12648 мой номер", "9643102303300612648"},
		{"Проверь 9643102303300612648, пожалуйста", "9643102303300612648"},
		{"Проверь 9643 102303300612648, пожалуйста", "9643102303300612648"},
		{"964310230330061264813", ""},
		{"1239643102303300612648", ""},
		{"9643102303300612648sdf", ""},
		{"xx9643102303300612648", ""},
		{"xx9643102303300612648", ""},
		{"9643-10230-33006-12648", ""},
		{"", ""},
	}

	for _, c := range cases {
		got := extractNumber(c.in)

		if got != c.want {
			t.Errorf("extractNumber(%q) == %q, want %q", c.in, got, c.want)
		}
	}
}
